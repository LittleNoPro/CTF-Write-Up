def _GCD(a, b):
    while not b.is_zero():
        _, r = a.quo_rem(b)
        a, b = b, r
    return a.monic()


for k in range(256):
    # f1(x) = (256*x + k)^e - c1
    # f2(x) = x^e - c2

    f1 = (256 * x + k)**e - c1
